param(
    [Parameter(Mandatory = $true)]
    [string]$ProjectPath,

    [Parameter(Mandatory = $true)]
    [string]$ProjectDir,

    [string]$RootNamespace,

    [Parameter(Mandatory = $true)]
    [string]$SourceFilesPath,

    [string]$AutoFix = 'false'
)

$ErrorActionPreference = 'Stop'

function Convert-ToNamespacePart {
    param([string]$Value)

    $part = [regex]::Replace($Value, '[^A-Za-z0-9_]', '_')

    if ($part -match '^[0-9]') {
        return "_$part"
    }

    return $part
}

function Get-RelativePath {
    param(
        [string]$BasePath,
        [string]$TargetPath
    )

    $baseFullPath = [System.IO.Path]::GetFullPath($BasePath).TrimEnd('\', '/') + [System.IO.Path]::DirectorySeparatorChar
    $targetFullPath = [System.IO.Path]::GetFullPath($TargetPath)
    $baseUri = New-Object System.Uri($baseFullPath)
    $targetUri = New-Object System.Uri($targetFullPath)

    return [System.Uri]::UnescapeDataString($baseUri.MakeRelativeUri($targetUri).ToString()).Replace('/', [System.IO.Path]::DirectorySeparatorChar)
}

function Test-IsGeneratedFile {
    param(
        [string]$FilePath,
        [string]$Content
    )

    $fileName = [System.IO.Path]::GetFileName($FilePath)

    return $fileName.EndsWith('.g.cs', [System.StringComparison]::OrdinalIgnoreCase) `
        -or $fileName.EndsWith('.Designer.cs', [System.StringComparison]::OrdinalIgnoreCase) `
        -or $Content.StartsWith('// <auto-generated/>', [System.StringComparison]::OrdinalIgnoreCase) `
        -or $Content.StartsWith('// <auto-generated>', [System.StringComparison]::OrdinalIgnoreCase)
}

function Get-ExpectedNamespace {
    param(
        [string]$FilePath,
        [string]$BaseDir,
        [string]$NamespaceRoot
    )

    $relativePath = Get-RelativePath -BasePath $BaseDir -TargetPath $FilePath
    $relativeDirectory = [System.IO.Path]::GetDirectoryName($relativePath)

    if ([string]::IsNullOrWhiteSpace($relativeDirectory)) {
        return $NamespaceRoot
    }

    $namespaceParts = $relativeDirectory -split '[\\/]' |
        Where-Object { -not [string]::IsNullOrWhiteSpace($_) } |
        ForEach-Object { Convert-ToNamespacePart $_ }

    if ($namespaceParts.Count -eq 0) {
        return $NamespaceRoot
    }

    return "$NamespaceRoot.$($namespaceParts -join '.')"
}

function Convert-ToBoolean {
    param([string]$Value)

    return $Value -eq 'true' -or $Value -eq 'True' -or $Value -eq '1'
}

if ([string]::IsNullOrWhiteSpace($RootNamespace)) {
    $RootNamespace = [System.IO.Path]::GetFileNameWithoutExtension($ProjectPath)
}

$shouldAutoFix = Convert-ToBoolean $AutoFix
$projectDirectory = [System.IO.Path]::GetFullPath($ProjectDir)
$excludedDirectories = @(
    [System.IO.Path]::Combine($projectDirectory, 'bin'),
    [System.IO.Path]::Combine($projectDirectory, 'obj')
)

$sourceFiles = Get-Content -LiteralPath $SourceFilesPath |
    Where-Object { -not [string]::IsNullOrWhiteSpace($_) } |
    ForEach-Object { [System.IO.Path]::GetFullPath($_) } |
    Where-Object {
        $file = $_
        (Test-Path -LiteralPath $file) -and
        -not ($excludedDirectories | Where-Object { $file.StartsWith($_, [System.StringComparison]::OrdinalIgnoreCase) })
    }

$hasErrors = $false

foreach ($file in $sourceFiles) {
    $content = Get-Content -LiteralPath $file -Raw

    if (Test-IsGeneratedFile -FilePath $file -Content $content) {
        continue
    }

    $namespaceMatch = [regex]::Match($content, '(?m)^\s*namespace\s+([A-Za-z_][A-Za-z0-9_]*(?:\.[A-Za-z_][A-Za-z0-9_]*)*)\s*(?:[;{])')

    if (-not $namespaceMatch.Success) {
        continue
    }

    $actualNamespace = $namespaceMatch.Groups[1].Value
    $expectedNamespace = Get-ExpectedNamespace -FilePath $file -BaseDir $projectDirectory -NamespaceRoot $RootNamespace

    if ($actualNamespace -ne $expectedNamespace) {
        $relativePath = Get-RelativePath -BasePath $projectDirectory -TargetPath $file
        $lineNumber = ($content.Substring(0, $namespaceMatch.Index) -split "`r`n|`n|`r").Count + 1

        if ($shouldAutoFix) {
            $updatedContent = $content.Remove($namespaceMatch.Groups[1].Index, $namespaceMatch.Groups[1].Length)
            $updatedContent = $updatedContent.Insert($namespaceMatch.Groups[1].Index, $expectedNamespace)
            $utf8NoBom = New-Object System.Text.UTF8Encoding($false)

            [System.IO.File]::WriteAllText($file, $updatedContent, $utf8NoBom)
            Write-Output "$file($lineNumber,1): warning NS0001: $relativePath namespace '$actualNamespace' was updated to '$expectedNamespace'."

            continue
        }

        $hasErrors = $true
        Write-Output "$file($lineNumber,1): error NS0001: $relativePath namespace '$actualNamespace' does not match folder path. Suggested fix: replace it with 'namespace $expectedNamespace'."
    }
}

if ($hasErrors) {
    exit 1
}
